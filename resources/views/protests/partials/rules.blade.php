    <h2 class="text-lg font-bold my-4 uppercase">Protesting On-Track Incidents</h2>
    <ul class="list-disc ml-8 mb-4">
        <li>On track incidents should be avoided at all costs.</li>
        <li>FRL's Racing Rules are in effect for all official races through the season.</li>
        <li>On track incidents should be reported to the Stewards</li>
    </ul>

    <h2 class="text-lg font-bold my-4 uppercase">Racing Rules</h2>

    <h3 class="text-md font-semi-bold my-2 uppercase">1. General</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>All drivers are required to read, understand, and abide by these rules. Please contact the Stewards if
            you do not understand a rule.
        </li>
        <li>All FRL members are expected to treat other members with respect. Verbal abuse and swearing are not
            allowed on voice or text chat.
        </li>
        <li>Cheating is not permitted under any circumstances.</li>
        <li>All FRL drivers must understand some basic English, or comfortable enough to use Google Translator on
            their own if needed.
        </li>
        <li>You are not permitted to talk during qualifying or the race in the game voicechat unless absolutely
            necessary.
        </li>
        <li>Our Discord server is free to use for all drivers but we are not responsible if someone is talking in
            the room you are in. You can contact your organizer if you want to create a public or private channel
            for your team.
        </li>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">2. Race Participation</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>All event participants must be active members of FRL.</li>
        <li>You are not permitted to change constructors during the season.</li>
        <li>Please be courteous and notify us if you are withdrawing from the league. Please don’t just stop showing up.
            If you know you will miss a race, you are required to notify the Organiser, or post in the race thread
            announcing your absence. We are more tolerant towards pilots who inform us of their absence.
        </li>
        <li>If you miss 2 races in a row without notifying the Organiser, you will forfeit your seat to the first driver
            on the waiting list, and if you wish to race again, you will be placed at the bottom of the waiting list.
        </li>
        <li>If you miss 3 races in a row with notifying the Organiser you will forfeit your seat to the first driver on
            the waiting list, and if you wish to race again, you will be placed at the bottom of the waiting list.
        </li>
        <li>If you need to retire during a race for any reason, you must inform the Organizer of the reason otherwise it
            will be logged as a rage-quit.
        </li>
    </ol>


    <h3 class="text-md font-semi-bold my-2 uppercase">3. Connections, Disconnections, & Restarts</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>A race will only be restarted if the conditions of a Mass Disconnect are met. A Mass Disconnect is defined
            if half of the field is disconnected during a specific window of time, and before the halfway point of the
            race. A race will not be restarted for a fewer amount of drivers, regardless if a lap has been completed or
            not.
        </li>
        <li>At any point if a driver is disconnected, they are allowed to reconnect and rejoin the race. The time and
            laps lost during a disconnection will not be given back to the driver automatically.
        </li>
        <li>Should technical issues prevent the race from taking place, it will be rescheduled for a break week, or
            after the season. No races will be cancelled unless there is absolutely no way to fit it in before the start
            of the next Season.
        </li>
    </ol>


    <h3 class="text-md font-semi-bold my-2 uppercase">4. Qualifying</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>When you are on your out lap in qualifying, you must try your best to not affect another driver who may be
            on their hot lap. This goes for both yielding to cars approaching from behind, and refraining from
            overtaking cars on their hot lap.
        </li>
        <li>In Qualifying, each driver is responsible for ensuring there is enough track space to complete their hot
            lap. It is not the responsibility of any driver ahead who may also be on their hot lap.
        </li>
        <li>Normal corner rights rules apply in qualifying when attempting to overtake another driver.</li>
    </ol>


    <h3 class="text-md font-semi-bold my-2 uppercase">5. Pit Etiquette</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>When you exit the pit-lane, you are not permitted to cross the dedicated pit line. Cars on the race track
            have right-of-way over cars entering the race track from the pits.
        </li>
        <li>If you intend to pit, but are alongside another driver at the pit entrance and they are blocking your
            entrance to the pits, it is your responsibility to yield by slowing down. Do not swerve in front of other
            drivers to enter the pits.
        </li>
    </ol>


    <h3 class="text-md font-semi-bold my-2 uppercase">6. Car Contact</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>Contacts and collisions must be avoided.</li>
        <li>Bump passes are not permitted. Bump passes are defined as the trailing car creating contact with the leading
            car in order to gain the preferred line and emerge in front.
        </li>
        <li>If contact between two drivers occurs, the trailing driver will be deemed responsible for the contact unless
            the contact is caused by the leading driver breaking a rule specified in these regulations or losing control
            of their vehicle.
        </li>
        <li>If you can prevent an incident by steering into the dirt or grass, you are expected to do so.</li>
        <li>If contact between drivers occurs that results in the guilty driver making up places on the innocent driver,
            the guilty driver must immediately relinquish that position; even if it means the guilty driver has to allow
            drivers who were not involved in the incident to pass while waiting for the driver they created contact with
            to resume racing.
        </li>
        <li>If you have to relinquish a position, move off the racing line before you slow down. Coming to a full stop
            is not allowed. Continue at the slower pace until the other driver passes you.
        </li>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">7. Corner Rights</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>If a car has sufficient overlap at the turn-in point of the corner, then cornering room must be given by
            both cars to allow the cars to take the corner together.
        </li>
        <ol class="list-decimal ml-8 mb-4">
            <li>‘Sufficient overlap’ or ‘overlap’ is defined as having any part your front wheels alongside the rear
                wheels of the car ahead. A following car has right to it’s established line any time that it has
                overlap. Leading or defending cars must yield to this right to road.
            </li>
            <li>In order to be entitled to space at corner exit, the following car must maintain overlap throughout the
                corner. Having overlap at turn-in does not grant you space entitlement at corner exit by default. If
                sufficient overlap is not kept throughout the corner then the leading car has the right to choose any
                line he wishes at the corner exit and the trailing car must then yield.
            </li>
            <li>If 2 cars enter a corner with overlap and that overlap is maintained throughout the entire corner, the
                car on the inside must maintain an inside line throughout the entire corner and the car on the outside
                must maintain an outside line throughout the entire corner. If any car narrows or tightens his radius
                which results in him ‘crossing lines’ from outside to inside or vice versa, that driver will be at fault
                for any contact that ensues.
            </li>
        </ol>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">9. Defending Your Position</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>More than one change of direction to defend a position is not permitted. Any driver moving back towards the
            racing line, having earlier defended his position off-line, should leave at least one car width between his
            own car and the edge of the track on the approach to the corner.
        </li>
        <li>Any driver defending their position on a straight, and before any braking area, may use the full width of
            the track during his first move, provided no significant portion of the car attempting to pass is alongside
            his.
        </li>
        <li>For the avoidance of doubt, if any part of the front wheel of the car attempting to pass is alongside the
            rear wheel of the car in front this will be deemed to be a ‘significant portion’.
        </li>
        <li>Blocking is not permitted at any time.</li>

        <ol class="list-decimal ml-8 mb-4">
            <li>Any deviation by a driver from the racing line, that is a reaction to a move from the trailing driver
                which impedes the trailing drivers forward progress, is not permitted at any time.
            </li>
            <li>You are entitled to choose your racing line, and positioning your car on the inside line is a fair
                manner to defend your position. However, you must position your car on this defensive line before the
                trailing driver attempts to pass you, and not as a reaction to his attempt.
            </li>
            <li>The ahead driver must not make any malicious or inappropriate braking or slowing maneuvers. Leading
                drivers must never 'brake test' the car(s) behind them, swerve in a braking zone, or try to break a tow.
                The leading driver is entitled to be slower or use longer and earlier braking zones than the trailing
                driver. They are also entitled to use whichever racing line they choose.
            </li>
        </ol>

        <li>Maneuvers liable to hinder other drivers, such as deliberate crowding of a car beyond the edge of the track
            or any other abnormal change of direction, are not permitted.
        </li>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">10. Being Lapped</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>As soon as a driver is caught by another driver who is about to lap him, the driver who is about be lapped
            must allow the lapping driver past at the first available opportunity. He must do this safely, and with
            minimal disruption. He should be predictable, and he shouldn’t brake hard or quickly change his racing line.
        </li>
        <li>If you have been lapped due to a pit cycle, you can only attempt to unlap yourself if the lapping car
            clearly loses pace or makes a mistake which allows you to pass. However, if the lapping car regains pace and
            catches back up to you, you must again allow it to pass.
        </li>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">11. Recovering from an Incident</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>A driver recovering from an incident and returning to the track must not impede any other car who is running
            in a proper racing line.
        </li>
        <li>If there is not an opportunity to safely rejoin the track for several seconds, you must wait until it is
            safe to do so.
        </li>
        <li>If two or more cars involved in a position battle together leave the track for any reason, they must rejoin
            in the order they left the track and without gaining an advantage.
        </li>
    </ol>


    <h3 class="text-md font-semi-bold my-2 uppercase">12. Track Limits</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>Drivers must use the track at all times. For the avoidance of doubt, the white lines defining the track
            edges are considered to be part of the track but the kerbs are not.
        </li>
        <li>A driver will be judged to have left the track if no part of the tires remains in contact with the track.
        </li>
        <li>Corner cutting is generally policed by iRacing, but repeated and excessive corner cutting and taking
            advantage of a sim flaw can be protested and sent to the Stewards.
        </li>
        <li>Should a car leave the track the driver may rejoin, however, this may only be done when it is safe to do so
            and without gaining any advantage.
        </li>
        <li>A driver may not deliberately leave the track without justifiable reason.</li>
        <li>If a position is gained by running off track, you must relinquish that position.</li>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">13. Wait List/ Reserve Driver Assignment</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li>Wait List drivers can only be assigned to a vacant Full Time seat by an Organiser or Manager.</li>
        <li>If one seat becomes available, the first available Wait List will be assigned to it.</li>
        <li>Reserve or Part Time drivers will not be used to fill spots in championships in which drivers are asked to
            pay a entry fee to help cover broadcasting fees.
        </li>
    </ol>

    <h3 class="text-md font-semi-bold my-2 uppercase">14. Championship Tie-Breaker</h3>

    <ol class="list-decimal ml-8 mb-4">
        <li> In the event where 2 Drivers are tied in points at the end of a Season, the Driver with the most Wins in that Season will be awarded the position in the Final Standings. If both Drivers have the same amount of Wins, then the Driver with the most 2nd Place finishes will be awarded the position in the Final Standings. This can continue on as needed (3rd, 4th, 5th, ect.) until the tie is broken.</li>
        <li>In the event where 2 Teams are tied in points at the end of a Season, the Team with the most Driver Wins in that Season will be awarded the position in the Final Standings. If both Teams have the same amount of Driver Wins, then the Team with the most Podiums/Top 5s finishes will be awarded the position in the Final Standings. If the Teams are still tied at this point, then the Team with the highest finishing Driver in the Driver Standings will be awarded the tie-breaker.</li>
    </ol>
